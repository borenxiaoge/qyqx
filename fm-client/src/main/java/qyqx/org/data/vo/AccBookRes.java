package qyqx.org.data.vo;

public class AccBookRes {

    private String accBookId;

    private String accBookName;

    private String openId;

    private String userId;

    public String getAccBookId() {
        return accBookId;
    }

    public void setAccBookId(String accBookId) {
        this.accBookId = accBookId;
    }

    public String getAccBookName() {
        return accBookName;
    }

    public void setAccBookName(String accBookName) {
        this.accBookName = accBookName;
    }

    public String getOpenId() {
        return openId;
    }

    public void setOpenId(String openId) {
        this.openId = openId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
